package prueba2;


public class Cabeza_LA {
	
	private String ojos_la;
	private String craneo_la;
	private String boca_la;
	private String nariz_la;
	
	public Cabeza_LA(String ojos_la, String craneo_la, String boca_la, String nariz_la) {
		this.ojos_la = ojos_la;
		this.craneo_la = craneo_la;
		this.boca_la = boca_la;
		this.nariz_la = nariz_la;
	}



	public String getOjos_la() {
		return ojos_la;
	}



	public void setOjos_la(String ojos_la) {
		this.ojos_la = ojos_la;
	}



	public String getCraneo_la() {
		return craneo_la;
	}



	public void setCraneo_la(String craneo_la) {
		this.craneo_la = craneo_la;
	}



	public String getBoca_la() {
		return boca_la;
	}



	public void setBoca_la(String boca_la) {
		this.boca_la = boca_la;
	}



	public String getNariz_la() {
		return nariz_la;
	}



	public void setNariz_la(String nariz_la) {
		this.nariz_la = nariz_la;
	}

	

}
