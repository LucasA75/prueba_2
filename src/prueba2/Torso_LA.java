package prueba2;

public class Torso_LA {

	private String motorCorazon_la;
	private String pecho_la;
	private String barriga_la;
	public String getMotorCorazon_la() {
		return motorCorazon_la;
	}
	public void setMotorCorazon_la(String motorCorazon_la) {
		this.motorCorazon_la = motorCorazon_la;
	}
	public String getPecho_la() {
		return pecho_la;
	}
	public void setPecho_la(String pecho_la) {
		this.pecho_la = pecho_la;
	}
	public String getBarriga_la() {
		return barriga_la;
	}
	public void setBarriga_la(String barriga_la) {
		this.barriga_la = barriga_la;
	}
	public Torso_LA(String motorCorazon_la, String pecho_la, String barriga_la) {
		super();
		this.motorCorazon_la = motorCorazon_la;
		this.pecho_la = pecho_la;
		this.barriga_la = barriga_la;
	}
	
	
}
